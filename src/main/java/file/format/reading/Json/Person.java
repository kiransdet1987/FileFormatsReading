package file.format.reading.Json;

public class Person {
	  private String name;
	  private int age;
	  private String city;
	  private Occupation occupation;
	  private String title;
	  
	public String getTitle() {
		return title;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public String getCity() {
		return city;
	}

	public Occupation getOccupation() {
		return occupation;
	}

}
